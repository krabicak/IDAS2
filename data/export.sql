           (to_timestamp(p_do, 'hh24:mi') > to_timestamp(POCATEK, 'hh24:mi') and
            to_timestamp(p_do, 'hh24:mi') <= to_timestamp(KONEC, 'hh24:mi')));
    return v_count;
  END IS_ROOM_EMPTY;

