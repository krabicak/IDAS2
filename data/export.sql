  CREATE OR REPLACE EDITIONABLE FUNCTION "C##ST52512"."IS_USER_ADMIN" (
  mail   IN VARCHAR2,
  passwd IN VARCHAR2
)
  RETURN BOOLEAN AS
  v_role pracovnici.role_zkratka%TYPE := NULL;
  v_hash NVARCHAR2(100) := passwd;
  BEGIN
    SELECT role_zkratka
        INTO
          v_role FROM pracovnici WHERE email = mail
                                   AND heslo = ENCRYPT_PASSWORD(v_hash);
    IF
    v_role != 'admin'
    THEN
      RETURN false;
    END IF;
    RETURN true;
    EXCEPTION
    WHEN no_data_found
    THEN
      RETURN false;
  END is_user_admin;

